import { ApptServices } from "../services/apptsServices";
import { Request, Response } from 'express';

const apptService = new ApptServices();

//============================================================================================================================================================
class ApptController{
    //============================================================================================================================================================
    // define available appointment(by doctor)
    // 
    //============================================================================================================================================================
    async defineAvailableApppointments(req:Request, res: Response):Promise<void>{
        return apptService.defineAvailableAppointments(req,res);
    }


    //============================================================================================================================================================
    // view available appointments (by users)
    //============================================================================================================================================================
    async viewAvailableAppointments(req: Request, res: Response): Promise<void>{
        return apptService.viewAppointments(req, res);
    }

    //============================================================================================================================================================
    // view Booked Appointments (by doctor)
    //============================================================================================================================================================

    async viewBookedAppointments(req: Request, res:Response): Promise<void>{
        return apptService.viewBookedAppointments(req, res);
    }

}
//============================================================================================================================================================
export {ApptController};